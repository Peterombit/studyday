#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *curr_elem;

	curr_elem = lst;
	while (curr_elem)
	{
		(*f)(curr_elem);
		curr_elem = curr_elem->next;
	}
}
